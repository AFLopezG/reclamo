<script>
import {globalStore} from 'stores/globalStore'
  export default {
    name: 'LoginPage',
    data () {
      return {
        name: '',
        password: '',
        remember: false,
        typePassword: true,
        loading: false,
        store:globalStore()
      }
    },
    mounted () {
      console.log(this.$url)
      if (this.store.isLoggedIn) {
        this.$router.push('/navegador')
      }
    }, 
    methods: {
      login () {
        this.loading = true
        this.$api.post('login', {
          name: this.name,
          password: this.password
        }).then(res => {
          this.$q.notify({
            message: 'Bienvenido',
            color: 'positive',
            icon: 'check_circle',
            position: 'top'
          })
          console.log(res.data)
          this.store.user = res.data.user
          this.store.isLoggedIn = true
          this.$api.defaults.headers.common.Authorization = 'Bearer ' + res.data.token
          localStorage.setItem('tokenReclamo', res.data.token)
          this.$router.push('/navegador')
        }).catch(error => {
          console.log(error)
          this.$q.notify({
            message: error.response.data.message,
            color: 'negative',
            position: 'top',
            timeout: 2000
          })
        }).finally(() => {
          this.loading = false
        })
      }
    }
}
</script>
